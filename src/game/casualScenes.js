// 获取口语化场景（随机选择变体）
export function getCasualScene(eventId, choiceId = null) {
  const key = choiceId ? `${eventId}_${choiceId}` : eventId
  const variants = CASUAL_SCENES[key] || CASUAL_SCENES[eventId]
  
  if (!variants) {
    return null
  }
  
  const variant = randomChoice(variants)
  return {
    scene: variant.scene,
    thoughts: randomChoice(variant.thoughts)
  }
}
